package operasiperbandingan;

public class OperasiPerbandingan {
    public static void main(String[] args) {
        var a = 100;
        var b = 100;
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
